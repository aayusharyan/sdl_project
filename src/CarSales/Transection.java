    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        String name1=name.getText();
        String dno=dcno.getText();
        String P=Pin.getText();
        String pric = pri.getText();
        int p =Integer.parseInt(pric);
        Statement s;
      try {
           s = con.createStatement();
            ResultSet re=s.executeQuery("Select * from transection");
            while(re.next()){
                 String name = re.getString("name");
                 String cno  = re.getString("dno");
                 String pin  = re.getString("pin");
                 long amount=re.getLong("amount");
                if(name.matches(name1)&& dno.matches(cno)&&P.matches(pin))
                   {
                       if(p<amount)
                         {
                           amount=amount-p;
                        
                           JOptionPane.showMessageDialog(null, "transection completed");
                           compname c=new compname();
                           c.setVisible(true);
                           this.dispose();
                           break;
                        
                         }
                      else
                       {
                           JOptionPane.showMessageDialog(null, "Insufficent amount");
                           Transection t=new Transection(pric);
                           t.setVisible(true);
                           this.dispose();
                       }
                    
                  }
                else
                   {
                      JOptionPane.showMessageDialog(null, "!!!Trasaction unsucessful");
                   }
                 
            }
           } catch (SQLException ex) {
                      Logger.getLogger(Transection.class.getName()).log(Level.SEVERE, null, ex);
               }
      
    }//GEN-LAST:event_submitActionPerformed

